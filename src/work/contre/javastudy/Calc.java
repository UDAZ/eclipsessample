package work.contre.javastudy;

public class Calc {

	public static void main(String[] args) {
		// 算術演算子 + - * / %
		int a = 15;
		int b = 4;
		System.out.println(a+b);//足し算
		System.out.println(a-b);//引き算
		System.out.println(a*b);//かけ算
		System.out.println(a/b);//割り算
		System.out.println(a%b);//余り
	}

}

/*
 * クラス名の先頭は大文字にする。ex. SampleApp
 * メソッド名・変数名は小文字にする。ex. getValue
 * メソッド名は、動詞 + 名刺。ex. getValue
 * 定数名は、すべて大文字。ex. FLAG_ON
 */